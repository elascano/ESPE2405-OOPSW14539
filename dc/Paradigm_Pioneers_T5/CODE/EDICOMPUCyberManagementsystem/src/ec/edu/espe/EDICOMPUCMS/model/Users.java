
package ec.edu.espe.EDICOMPUCMS.model;


public class Users {
    String username;
    String password;

    public Users(String username, String password) {
        this.username = username;
        this.password = password;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }
    
    
}

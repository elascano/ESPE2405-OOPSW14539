    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    int selectedRow = tableAllSinger.getSelectedRow();
    if (selectedRow != -1) {
        int currentId = (int) tableAllSinger.getValueAt(selectedRow, 0);
        String name = (String) tableAllSinger.getValueAt(selectedRow, 1);
        String song = (String) tableAllSinger.getValueAt(selectedRow, 2);

        String newIdStr = JOptionPane.showInputDialog(this, "Editar ID:", currentId);
        String newName = JOptionPane.showInputDialog(this, "Editar nombre:", name);
        String newSong = JOptionPane.showInputDialog(this, "Editar canción:", song);

        if (newIdStr != null && newName != null && newSong != null) {
            try {
                int newId = Integer.parseInt(newIdStr);
                Houses updatedSinger = new Houses(newId, newName, newSong);
                saveManager.updateSinger(currentId, updatedSinger);
                updateTable();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "El ID debe ser un número entero válido.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Por favor, seleccione un cantante para editar.");
    }
    }//GEN-LAST:event_btnEditActionPerformed


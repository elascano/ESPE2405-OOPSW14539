    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       int selectedRow = tableAllSinger.getSelectedRow();
        if (selectedRow != -1) {
            int id = (int) tableAllSinger.getValueAt(selectedRow, 0);
            int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro de que desea eliminar este cantante?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                saveManager.deleteSinger(id);
                updateTable();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un cantante para eliminar.");
        }  
    }//GEN-LAST:event_btnDeleteActionPerformed


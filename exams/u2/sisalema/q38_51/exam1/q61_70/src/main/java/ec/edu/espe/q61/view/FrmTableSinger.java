    public void initializeTable() {
        defaultTableModel = new DefaultTableModel(
            new Object [][] {},
            new String [] {"ID", "Nombre", "Canción"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }
        };
        tableAllSinger.setModel(defaultTableModel);
    }

    public void updateTable() {
        defaultTableModel.setRowCount(0);
        List<Houses> singers = saveManager.getAllSingers();
        System.out.println("Número de cantantes recuperados: " + singers.size());
        for (Houses singer : singers) {
            defaultTableModel.addRow(new Object[]{
                singer.getId(), 
                singer.getName(), 
                singer.getSong()
            });
        }
        tableAllSinger.repaint();
    }


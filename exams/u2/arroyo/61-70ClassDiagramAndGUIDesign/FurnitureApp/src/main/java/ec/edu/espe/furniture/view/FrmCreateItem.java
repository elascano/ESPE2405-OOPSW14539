    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // Obtener los datos del formulario
        String id = txtID.getText().trim();
        String name = txtName.getText().trim();
        String priceText = txtPrice.getText().trim();
        int quantity = (int) spnQuantity.getValue();

        // Validar que los campos no estén vacíos y que el precio sea un número válido
        if (id.isEmpty() || name.isEmpty() || priceText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled!", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        float price;
        try {
            price = Float.parseFloat(priceText);
            if (price < 0) {
                JOptionPane.showMessageDialog(this, "Price must be a positive number.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Price must be a valid number.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Crear una instancia de Furniture
        Furniture furniture = new Furniture(id, name, price, quantity);

        // Crear una instancia de CloudDB y guardar los datos
        CloudDB cloudDB = new CloudDB();
        if (cloudDB.furnitureExists(id)) {
            JOptionPane.showMessageDialog(this, "A furniture item with this ID already exists.", "Duplicate ID", JOptionPane.WARNING_MESSAGE);
        } else {
            cloudDB.uploadFurnitureData(furniture);
            JOptionPane.showMessageDialog(this, "Furniture added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

            // Limpiar campos después de agregar
            txtID.setText("");
            txtName.setText("");
            txtPrice.setText("");
            spnQuantity.setValue(0);
        }

    }//GEN-LAST:event_btnCreateActionPerformed

    private void txtNameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNameKeyTyped
        char c = evt.getKeyChar();
        // Permite solo letras, espacios y caracteres de control
        if (!(Character.isLetter(c) || Character.isWhitespace(c) || Character.isISOControl(c))) {
            evt.consume(); 
            JOptionPane.showMessageDialog(this, c + " is not accepted here", "Warning on input data", JOptionPane.WARNING_MESSAGE);
            
        }
    }//GEN-LAST:event_txtNameKeyTyped


    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        try {
            int x = Integer.parseInt(txtRows.getText().trim());
            int y = Integer.parseInt(txtColumns.getText().trim());

            ASCIIPrinter maze = new ASCIIPrinter(x,y);
            maze.displayMaze();

            maze.resolveMaze();
            System.out.println("\n\n");

            maze.displayMaze();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid integers for Rows and Columns", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGenerateActionPerformed


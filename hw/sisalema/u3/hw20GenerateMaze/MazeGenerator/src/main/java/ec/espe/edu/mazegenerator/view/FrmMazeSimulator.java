    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    boolean isValid = false;
    int mazeWidth = 0;
    int mazeHeight = 0;  
    
    try {
        mazeWidth = Integer.parseInt(txtWidth.getText());
        mazeHeight = Integer.parseInt(txtHeight.getText());
        
        if(mazeWidth >= 4 && mazeHeight >= 4){
            isValid = true;   
        } else {
            JOptionPane.showMessageDialog(this, "Las dimensiones deben ser mayores o iguales a 4", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese números válidos", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    Maze maze = new Maze(mazeWidth, mazeHeight);
    RandomGenerator randomGenerator = new RandomGenerator();
    MazeGenerator mazeGenerator = new MazeGenerator(randomGenerator);
    MazePrinter mazePrinter = new MazePrinter();
    MazeController mazeController = new MazeController(maze, mazeGenerator, mazePrinter, randomGenerator);
    mazeController.createMaze();
    mazeController.printMaze();
    }//GEN-LAST:event_jButton1ActionPerformed


    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try {
            int size = Integer.parseInt(txtSize.getText().trim());
            String[] numberStrings = txtNumbers.getText().split(",");

            if (numberStrings.length != size) {
                JOptionPane.showMessageDialog(this,
                        "Error: Se esperaban " + size + " números, pero se encontraron " + numberStrings.length + " .",
                        "Error de Tamaño",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            int[] numbers = new int[size];

            for (int i = 0; i < size; i++) {
                numbers[i] = Integer.parseInt(numberStrings[i].trim());
            }

            SortingContext sc = new SortingContext();
            int[] sortedList = sc.sort(numbers);

            StringBuilder sortedNumbers = new StringBuilder();
            for (int num : sortedList) {
                sortedNumbers.append(num).append(" ");
            }
            txtSort.setText(sortedNumbers.toString());

        } catch (NumberFormatException ex) {

            JOptionPane.showMessageDialog(this,
                    "Error: Asegúrese de que todos los campos contengan números válidos.",
                    "Error de Formato",
                    JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Error: " + ex.getMessage(),
                    "Error Desconocido",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1MouseClicked


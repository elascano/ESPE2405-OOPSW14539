    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
     Animal animal;
    int id;
    String description;
    Date bornOnDate;
    String gender;
    String cage;
    int numberOfBones;
    boolean isVertebrate;
    ArrayList<String> foods = new ArrayList<>();

    id = Integer.parseInt(txtId.getText());
    description = areDescription.getText();
    bornOnDate = new Date();  // Puedes ajustar esto según el formato deseado para la fecha
    gender = cmbGender.getSelectedItem().toString();
    cage = cmbCage.getSelectedItem().toString();
    numberOfBones = sldNumberOfBones.getValue();
    isVertebrate = chkVertebrate.isSelected();
    foods.addAll(lstFood.getSelectedValuesList());

    animal = new Animal(id, description, bornOnDate, gender, cage, numberOfBones, isVertebrate, foods);

    // Convertir el objeto Animal a JSON usando Gson
    Gson gson = new Gson();
    String json = gson.toJson(animal);

    // Guardar el JSON en un archivo
    try (FileWriter fileWriter = new FileWriter("animal.json", true)) {  // Agregar `true` para que los datos se agreguen al final del archivo
        fileWriter.write(json + System.lineSeparator());  // Agregar una nueva línea después de cada registro
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al guardar el archivo JSON", "Error", JOptionPane.ERROR_MESSAGE);
    }

    // Mostrar mensajes de diálogo
    JOptionPane.showMessageDialog(this, "Animal registrado con éxito");
    
    JOptionPane.showMessageDialog(this, animal.getId()   +   animal.getDescription()   +   animal.getGender());
    JOptionPane.showMessageDialog(this, animal.getCage(), "Animal info " , JOptionPane.CANCEL_OPTION);
    JOptionPane.showConfirmDialog(this, "are you sure you want ot save animal? ----> " + animal.getDescription());
    }//GEN-LAST:event_btnRegisterActionPerformed
    

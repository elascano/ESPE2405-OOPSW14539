    private void guardarJson() {
        Map<String, Object> datos = new HashMap<>();
        datos.put("id", txtId.getText());
        datos.put("description", txtDescription.getText());
        datos.put("date", new Date());
        datos.put("gender", cmbGender.getSelectedItem().toString());
        datos.put("cage", cmbCage.getSelectedItem().toString());
        datos.put("numberOfBones", sldNumberBones.getValue());
        datos.put("isVertebrate", chkVertebrate.isSelected());
        datos.put("foods", lslFood.getSelectedValuesList());
        
        
        JsonGenerator.guardarJson(datos, "animal_data.json");
    }


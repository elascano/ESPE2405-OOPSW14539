    private void btmRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmRegisterActionPerformed
        Animal animal;
        int id;
        String description;
        Date date;
        String gender;
        String cage;
        int numberOfBones;
        boolean isVertebrete;
        List<String> foods = new ArrayList<>();
        
        id = Integer.parseInt(txtId.getText());
        description = txtDescription.getText();
        date = new Date();
        gender = cmbGender.getSelectedItem().toString();
        cage = cmbCage.getSelectedItem().toString();
        numberOfBones = sldNumberBones.getValue();
        isVertebrete = chkVertebrate.isSelected();
        foods = lslFood.getSelectedValuesList();
        
        
        animal= new Animal(id, description, date, gender, cage, numberOfBones, isVertebrete, (ArrayList < String >)foods);
        
        
        JOptionPane.showMessageDialog(this,animal.getId() + animal.getDescription() + animal.getGender() + animal.getNumberOfBones() );
        JOptionPane.showMessageDialog(this, animal.getCage(),"Animal info",JOptionPane.QUESTION_MESSAGE);
        JOptionPane.showConfirmDialog(this,"Are you want of save animal-->"+animal.getDescription());
        
         Document document = new Document()
            .append("id", id)
            .append("description", description)
            .append("date", date)
            .append("gender", gender)
            .append("cage", cage)
            .append("numberOfBones", numberOfBones)
            .append("isVertebrate", isVertebrete)
            .append("foods", foods);
         MongoCollection<Document> coleccion = database.getCollection("Animal");
                coleccion.insertOne(document); 
             
        guardarJson();  
        JOptionPane.showMessageDialog(this, "Animal registered successfully!");
    }//GEN-LAST:event_btmRegisterActionPerformed


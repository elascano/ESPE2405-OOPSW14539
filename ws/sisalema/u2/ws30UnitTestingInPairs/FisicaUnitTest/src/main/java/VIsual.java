    private static MongoDatabase mongodb = null;

   
     // MÃ©todo para conectar a la base de datos
    public static MongoDatabase connectionDataBase() {
        if (mongodb == null) {
            String uri = "mongodb+srv://pilaguano:pilaguano@cluster0.9goxw5i.mongodb.net/";
            String db = "Fisica";
            try {
                MongoClient mongoClient = MongoClients.create(uri);
                mongodb = mongoClient.getDatabase(db);
                System.out.println("Connected successfully to database");
            } catch (Exception e) {
                System.err.println("Error connecting to database: " + e.getMessage());
            }
        }
        return mongodb;
    }

    // MÃ©todo para guardar los datos en la base de datos
    public void save(String mass, String velocity) {
        MongoDatabase database = connectionDataBase();
        MongoCollection<Document> collection = database.getCollection("animals");

        // Crear el documento con los datos
        Document doc = new Document("mass", mass)
                .append("velocity", velocity);
        
        // Insertar el documento en la colecciÃ³n
        collection.insertOne(doc);
        System.out.println("Guardado exitosamente");
    }

    
  

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
    Animal animal;
    int id;
    String description;
    Date date;
    String gender;
    String cage;
    int numberOfBones;
    boolean isVertebrate;
    ArrayList<String> foods = new ArrayList<>();
    
    id = Integer. parseInt(txtId.getText());
    description = Description.getText ();
    date = new Date();
    gender = cmbGender.getSelectedItem().toString();
    cage = cmbCage.getSelectedItem().toString();
    numberOfBones =sldNumberOfBones.getValue();
    isVertebrate = chkVertebrate.isSelected();
    foods = (ArrayList<String>) lstfood.getSelectedValuesList();
    animal = new Animal(id,description,date,gender,cage,numberOfBones,isVertebrate,(ArrayList<String>)foods);
    
    JOptionPane.showMessageDialog(this,animal.getId()+ animal.getDescription()+animal.getGender()+animal.getNumberOfBones());
    JOptionPane.showMessageDialog(this,animal.getCage(),"Animal Info",JOptionPane.CANCEL_OPTION);
    JOptionPane.showMessageDialog(this,"are you sure you want to save animal -->"+animal.getDescription());
        // TODO add your handling code here:{
    JOptionPane.showConfirmDialog(this,"txt" + animal.getId());
    
    SaveManager saveManager = new SaveManager();
    try {
        saveManager.saveAnimal(animal);
        JOptionPane.showMessageDialog(this, "Animal guardado en MongoDB: " + animal.getDescription());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al guardar el animal: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnRegisterActionPerformed

